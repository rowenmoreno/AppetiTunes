package com.moreno.searchitunes.models;

public class Track {
    String trackName;
    String trackArtwork;
    String trackPrice;

    public String getTrackName() {
        return trackName;
    }

    public void setTrackName(String trackName) {
        this.trackName = trackName;
    }

    public String getTrackArtwork() {
        return trackArtwork;
    }

    public void setTrackArtwork(String trackArtwork) {
        this.trackArtwork = trackArtwork;
    }

    public String getTrackPrice() {
        return trackPrice;
    }

    public void setTrackPrice(String trackPrice) {
        this.trackPrice = trackPrice;
    }
}
